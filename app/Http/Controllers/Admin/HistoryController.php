    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        $history = History::where('id',$id)->with('user','products')->firstOrFail();
        foreach($history->products as $product){
            $productQt = Product::find($product->id)
                ->join('history_product','products.id','=','history_product.product_id')
                ->where('history_product.history_id', '=',$id)
                ->get();
        }
        return view('admin.history.details',compact('history','productQt'));
    }

